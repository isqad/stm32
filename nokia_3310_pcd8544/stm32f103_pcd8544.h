#include "stm32f10x.h"

#define PCD8544_RST_RCC        RCC_APB2ENR_IOPCEN
#define PCD8544_CE_RCC         RCC_APB2ENR_IOPCEN
#define PCD8544_DC_RCC         RCC_APB2ENR_IOPCEN
//
#define PCD8544_WIDTH          84
#define PCD8544_HEIGHT         48

// PCD8544 Commandset
// Instruction set
#define PCD8544_DISPLAYCONTROL 0x08 // 0b00001000
#define PCD8544_FUNCTIONSET    0x20 // 0b00100000
#define PCD8544_SETYADDR       0x40 // 0b01000000
#define PCD8544_SETXADDR       0x80 // 0b10000000
// General Commands
#define PCD8544_PWRDOWN        0x04
#define PCD8544_ENTRYMODE      0x02
#define PCD8544_EXTINSSET      0x01
#define PCD8544_DISPLAYBLANK   0x00
#define PCD8544_NORMALMODE     0x02
#define PCD8544_ALLSEGMENTSON  0x01
#define PCD8544_INVERSEVEVIDEO 0x03

#define PCD8544_BUFFER_SIZE    PCD8544_WIDTH * PCD8544_HEIGHT / 8
